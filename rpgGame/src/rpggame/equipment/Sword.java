package rpggame.equipment;

public class Sword {
	// フィールドの宣言
	private String name;
	private int damage;

	// Getter、Setterを定義
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getDamage() {
		return damage;
	}
	public void setDamage(int damage) {
		this.damage = damage;
	}
}
